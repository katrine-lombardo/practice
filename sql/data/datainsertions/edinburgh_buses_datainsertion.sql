-- CREATE TABLES
CREATE TABLE IF NOT EXISTS stops (
    id INTEGER PRIMARY KEY,
    name VARCHAR(30)
);

CREATE TABLE IF NOT EXISTS route (
    num VARCHAR(5),
    company VARCHAR(3),
    pos INTEGER,
    stop INTEGER,
    FOREIGN KEY (stop) REFERENCES stops(id)
);

